import Foundation

/**
 🧠 ШПАРГАЛКА ПО УСЛОВИЯМ В SWIFT
 Контроль потока без багов и с душой

 1. 🚦 IF-ELSE (Классика жанра)
 Золотое правило: Только Bool! Никаких if number (как в C или JS) — язык со строгой типизацией вас не пустит.

 ✅ if age > 18 { ... }

 ❌ if age { ... } (Ошибка компиляции!)

 ⚠️ Ловушка с присваиванием: Одиночное равно = внутри условия запрещено. Только сравнение ==.
 if x = 5 { ... } — не скомпилируется, даже если x это переменная (var). В Swift операция присваивания не возвращает значения. Это защищает от случайных опечаток.

 🧠 Термин с собеса: Short-circuit evaluation (Краткое вычисление). Если вы связываете условия через && (И) и первое условие оказалось false, Swift вообще не будет проверять второе, так как итог уже понятен. Это экономит ресурсы процессора.

 2. ❓ ТЕРНАРНЫЙ ОПЕРАТОР ? : (Краткость)
 Фишка: Это не просто сокращение, это expression (выражение), которое возвращает готовое значение.

 Синтаксис:
 let result = условие ? значение_если_True : значение_если_False

 💡 Пример:

 swift
 let status = age >= 18 ? "🍺 Доступно" : "🧃 Только сок"
 🚨 Нюанс: Не вкладывайте тернарники друг в друга — это больно читать. Используйте только для простых, очевидных присваиваний в одну строку.

 3. 🔀 SWITCH (Мощь и безопасность)
 🔥 Исчерпываемость (Exhaustiveness)
 switch обязан покрывать 100% вариантов. Поэтому либо вы перечисляете абсолютно все case, либо в конце ставите default.

 💎 Исключение из правил: Если вы перебираете enum (перечисление) и указали все его кейсы, default писать не нужно. Если вы его напишете, компилятор не сможет предупредить вас, когда в будущем в этот enum добавят новый вариант.

 🚫 Провал вниз (Fallthrough)
 В Swift по умолчанию нет автоматического провала (как в других языках). Нашелся подходящий case — выполнился код — Swift вышел.
 Чтобы принудительно заставить программу упасть в следующий case (даже если его условие не совпадает), пишите ключевое слово fallthrough.

 🎨 Диапазоны
 Помните разницу между закрытым 1...10 (включает 10) и полуоткрытым 1..<10 (до 9 включительно) диапазонами. Диапазоны в разных case могут пересекаться — Swift выполнит тот, что лежит выше по коду.

 🎭 Wildcard _ (Игнорирование)
 Если в кортеже одно из значений не важно:
 case (_, 5): — сработает для любой точки, где второе число равно 5.

 🧬 Value Binding + where (Магия)
 Вытаскиваете значения прямо в локальные константы и фильтруете их "на лету":

 swift
 switch point {
 case (let x, let y) where x == y:
     print("🎯 На диагонали: \(x)")
 case (let x, 0):
     print("📐 На оси X: \(x)")
 default:
     break
 }
 🚀 Фишка Swift 5.9+: Switch как выражение
 Теперь switch умеет напрямую возвращать значение и присваивать его переменной:

 swift
 let grade = switch testScore {
     case 90...100: "A"
     case 80..<90: "B"
     default: "C"
 }
 4. 🛡️ GUARD (Ранний выход / Bodyguard)
 Философия: Проверяем идеальное ("хорошее") условие. Если всё пошло не так — уходим в блок else.

 🛑 Жесткое ограничение: guard не умеет ветвиться. У него не бывает блоков else if. Только одно условие и один else.

 🧱 Строгое правило: Внутри else обязательно должен быть сквозной выход из текущего скоупа (области видимости). Компилятор не пропустит код без:

 return (выход из функции)

 break или continue (выход/переход в цикле)

 throw (выброс ошибки)

 fatalError() (краш приложения, если всё совсем плохо)

 💡 Главный плюс: Безопасно разворачивает опционалы (guard let) и создает переменные, которые живут ПОСЛЕ блока guard во всей оставшейся части функции. Это избавляет код от "пирамид смерти" (вложенных if let).

 swift
 func process(name: String?) {
     guard let userName = name, !userName.isEmpty else {
         print("👋 Имя невалидно — выходим")
         return
     }
     // Переменная userName доступна здесь и гарантированно существует!
     print("Привет, \(userName)!")
 }
 📌 БОНУС: Сравнительная таблица (Когда что юзать?)
 Конструкция    Лучшее применение    Суперсила    Сложность
 if-else    Обычные проверки (2–3 ветки)    Читаемость и простота    ⭐
 ? :    Присвоение значения переменной в 1 строку    Предельная лаконичность    ⭐⭐
 switch    Много вариантов / Работа с Enum / Кортежи    Исчерпываемость + гибкие фильтры where    ⭐⭐⭐
 guard    Проверка входных данных на старте функции    Чистый код без вложенных скобок ("ранний выход")    ⭐⭐⭐
 
 ⚡️ ФИНАЛЬНЫЙ ЧЕК-ЛИСТ (Перед собеседованием)
 □ if — только Bool, никаких чисел
 □ = и == не путать
 □ switch — всегда покрывай все варианты (или default)
 □ fallthrough — только когда осознанно нужно
 □ guard — всегда с return/break/throw в else
 □ Swift 5.9+ — switch может возвращать значение
*/
