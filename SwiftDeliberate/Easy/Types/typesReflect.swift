 ## 📝 РЕФЛЕКСИЯ: Мои грабли и инсайты
 
 1. Что зафиксировано:
    - Для изменения `Bool` на противоположное значение использую `.toggle()`. Это лаконично и читаемо.
    - Кортежи (`Tuples`) — это простые группы значений. Если кортеж создан через `var`, его элементы можно перезаписывать по отдельности.
    - Из кортежа можно вытаскивать значения по именам (`airport.city`) или раскладывать в новые переменные (`let (_, price) = product`). Значок `_` позволяет красиво игнорировать ненужные части.
 
 
  ## 📝 РЕФЛЕКСИЯ: Мои грабли (Middle)
  
  ### 1. Где споткнулся и что осознал:
  * **Ловушка Double:** Пытался сравнивать дроби «в лоб». Осознал, что из-за двоичной природы `Double` компьютер всегда накапливает микро-погрешность округления. Сравнение через `==` в проде — это гарантированный баг. Теперь только через `Double.ulpOfOne`.
  * **Защита от крашей (Переполнение):** Swift жестко роняет приложение при превышении лимитов типа (например, `Int.max + 1`). Это круто для безопасности. Если нужен цикличный сброс в ноль — использую специальный оператор `&+`.
  * **Копии массивов и структур:** Массивы — это структуры. Присвоив один массив другому, я создаю независимую копию. Изменение копии не трогает оригинал. Чтобы функция могла изменить оригинальную структуру, нужно явно использовать `inout` и передавать её через `&`.
  * **Кортежи как строгие типы:** Изменение порядка имен свойств в кортежах (`x:` и `y:`) превращает их для компилятора в разные типы данных. Сравнивать их напрямую нельзя — код даже не скомпилируется.
  
  ### 2. Ключевые слова для поиска:
  * Swift floating point tolerance, Arithmetic overflow operators (`&+`), Swift `inout` parameters.
 
  ## 📝 РЕФЛЕКСИЯ: Дополнительные инсайты из тестов
  
  * Инициализатор `Int("строка")` всегда возвращает ОПЦИОНАЛ `Int?`, так как строка может содержать некорректные символы.
  * Копирование классов через `=` копирует только ССЫЛКУ, но не сам объект. Меняя свойства копии, мы гарантированно меняем оригинал.
  * Тип `UInt` не знает, что такое минус. Вычитание из `0` приводит к немедленному рантайм-крашу.
  * Структуры нельзя мутировать (`mutating`) внутри асинхронных escaping-замыканий, так как стек может очиститься до выполнения кода.
  
