 /*
  ## ТЕОРИЯ: Типы данных (Easy)
  
  В этой теме зафиксированы базовые правила Swift
  
  ### 1. Строгая булевая логика (Bool)
  * В Swift числа `0` и `1` НЕ являются заменой для `false` и `true`.
  * Условия в операторах `if` или `guard` принимают исключительно чистый тип `Bool`.
    Код `if statusCode { ... }` не скомпилируется. Обязательно писать явное сравнение: `if statusCode == 1 { ... }`.
  * Для элегантного разворота флага `true -> false` или `false -> true` используется встроенный метод `.toggle()`. Он мутирует переменную на месте и избавляет от громоздкой записи `isReady = !isReady`.
  
  ### 2. Кортежи (Tuples)
  * Кортеж — это простейший способ временно объединить несколько значений разных типов в одну переменную БЕЗ создания отдельной структуры (`struct`).
  * Изменяемость: Если кортеж объявлен через `var`, мы можем менять его отдельные элементы (например, `airport.code = "SVO"`), при этом остальные элементы остаются нетронутыми. Если объявлен через `let` — он полностью константен.
  * Доступ к данным осуществляется тремя путями:
    1. По имени свойства: `product.price` (если имена были заданы при создании).
    2. По индексу: `product.1` (номера идут с нуля).
    3. Через деструктуризацию: `let (name, price) = product`. Если какое-то значение не нужно, его можно проигнорировать с помощью нижнего подчеркивания: `let (_, price) = product`.
 */
/*
 ## 🧠 ТЕОРИЯ: Типы данных (Middle)
 
 ### 1. Погрешность Double
 * Числа с плавающей точкой хранятся в двоичном виде с округлением. `0.1 + 0.2` дает `0.30000000000000004`.
 * Прямое сравнение через `== 0.3` провалится. Сравнивать нужно через модуль разности и системную погрешность: `abs(a - b) < Double.ulpOfOne`.
 
 ### 2. Арифметическое переполнение
 * Выход за границы типа (например, `Int.max + 1`) намертво крашит приложение для безопасности данных.
 * Если зацикливание числа необходимо по логике, используются операторы разрешенного переполнения: `&+`, `&-`, `&*`.
 
 ### 3. Передача по ссылке для структур (inout)
 * Структуры — это Value Types. В функциях они всегда копируются.
 * Чтобы функция изменила оригинал, параметр помечается как `inout`, а при вызове передается адрес через префикс `&`.
*/
