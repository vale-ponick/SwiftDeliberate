import Foundation
/* 📝 Важное дополнение к теории: Память массивов (Copy-on-Write)
 
 Главная суть:
 В Swift массивы ведут себя как типы-значения (при копировании создается независимая копия, оригинал испортить нельзя). Но чтобы программа не зависала при копировании больших массивов, Swift использует хитрый трюк — Copy-on-Write (Копирование при записи).
 Как это работает на пальцах:
 Простое копирование (Экономия): Когда мы пишем let/var B = A, Swift не копирует элементы в памяти. Оба массива тихонько смотрят на один и тот же общий буфер с данными.
 Момент изменения (Безопасность): Как только мы пытаемся изменить массив B (например, через .append()), Swift видит, что у памяти два хозяина. В эту же наносекунду он делает реальную физическую копию данных для массива B.
 Результат: Массивы разделяются. Массив A остался нетронутым, а B успешно изменился.
 Вывод: Мы пишем код спокойно, зная, что наши массивы изолированы друг от друга, а Swift сам заботится о том, чтобы это происходило максимально быстро и не забивало оперативную память.
 
 
 
  ## ArraySlice — ловушка памяти

  `.prefix(3)` и `.suffix(3)` возвращают `ArraySlice`, а не `Array`.

  ❌ Ошибка:
  
  let slice = prices.prefix(3)  // ArraySlice
 ✅ Правильно:

 let topPrices = Array(prices.prefix(3))  // Array
 */
 
/*
 ## ArraySlice — срезы

 - Срез — это «окно» в массив, без копирования данных
 - Создаётся через `array[start...]`, `array[..<end]`, `array[...end]`
 - Тип: `ArraySlice<Element>`
 - Чтобы получить массив: `Array(slice)`

 ### Опасность:
 - Срез держит ссылку на весь массив
 - Если массив большой, а срез жив — память не освободится

 ### Правильное копирование:
 - `let safeArray = Array(hugeArray[0...4])`
 - `hugeArray = []` — теперь память освободится

 ### Когда использовать:
 - Для быстрых операций без копирования
 - Когда нужна только часть данных
 */
