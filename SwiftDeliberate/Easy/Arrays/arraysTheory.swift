import Foundation
/*
 📐🚕💨 ТЕОРИЯ: Массивы (Easy)

 🎯 Главная задача:
 Массив — упорядоченная коллекция элементов одного типа.
 Доступ по индексу — O(1).

 📖 История:
 Массивы в Swift унаследованы от Objective-C (NSArray),
 но в Swift 1.0 (2014) стали типобезопасными структурами (value types).


 ⚙️ Как работает под капотом:

 Массив в Swift — это непрерывный блок памяти.
 Доступ по индексу — O(1).
 НО есть важный механизм: Copy‑on‑Write -> 'копирование при записи'.


 🔹 На пальцах (доступно):

 Представь, что у тебя есть коробка с вещами.
 - Когда ты говоришь: «Дай мне такую же коробку» — ты не копируешь вещи, а просто смотришь на ту же полку.
 - Ты и твой друг видите одну и ту же коробку. Это дёшево и быстро.
 - Но как только ты решаешь положить в неё что‑то своё — ты заводишь отдельную коробку.
 - Твоя коробка меняется, а у друга остаётся старая.

 В Swift то же самое:
 let a = [1, 2, 3]
 var b = a      // a и b — один и тот же массив в памяти
 b.append(4)    // только СЕЙЧАС создаётся копия

 Массив a остался [1, 2, 3], а b стал [1, 2, 3, 4].


 🔹 Инженерно (точно):

 - Массивы в Swift — это struct (value types).
 - При присваивании copy НЕ сам массив, а ССЫЛКА на буфер.
 - Буфер делится между экземплярами до первого изменения.
 - При изменении (.append, .insert, .remove и т.д.) создаётся физическая копия данных.
 - Это называется Copy‑on‑Write (CoW).
 - CoW позволяет экономить память и сохранять типобезопасность.
 
  🔹 Где живёт массив?

  Массив в Swift — это структура (value type), но его данные хранятся в куче (heap).
  Это сделано для того, чтобы массив мог динамически расти и меняться без копирования всей структуры.

  📌 Что такое куча (heap)?
  - Область памяти для динамических данных.
  - Данные живут, пока на них есть ссылки.
  - Управляется автоматически (ARC).

  📌 А что такое стек (stack)?
  - Быстрая память для временных данных (переменные внутри функций).
  - Данные удаляются при выходе из функции.

  📌 Что такое непрерывный блок?
  - Элементы массива хранятся в памяти **подряд**, без пропусков.
  - Это даёт быстрый доступ по индексу (O(1)).
  - Это называется **смежное хранение** (contiguous storage).


  🔹 Что такое ContiguousArray?

  `ContiguousArray` — это массив, который **гарантированно хранит элементы в непрерывном блоке памяти**.

  🆚 Отличие от обычного Array:
  | Тип | Где хранится | Особенность |
  |-----|-------------|-------------|
  | Array | Куча, но может использовать мост к Objective‑C | Может быть «неконтигуозным» при взаимодействии с ObjC |
  | ContiguousArray | Куча, строго непрерывно | Гарантирует непрерывную память, быстрее в некоторых операциях |

  🧠 Когда использовать:
  - Обычный Array — в 99% случаев.
  - ContiguousArray — когда нужна максимальная производительность и ты работаешь только со Swift (без ObjC).

  📌 Итог:
  - Array — гибкий, но может иметь накладные расходы.
  - ContiguousArray — предсказуемый и быстрый, но реже нужен.
  - Непрерывный блок памяти — основа быстрого доступа по индексу.

 🧠 GOLDEN RULE:

 Массив ведёт себя как value type, НО внутри использует оптимизацию CoW.
 Мы пишем код так, как будто массив копируется всегда, а Swift заботится о производительности.

 📌 ИТОГ:
 - До изменения — общая память (экономия).
 - При ПЕРВОМ изменении —> КОПИЯ (безопасность).
 - Swift делает это auto. Мы просто работаем с массивом.


 🔹 ArraySlice — ловушка памяти:

 - `.prefix(3)` и `.suffix(3)` возвращают `ArraySlice`, а НЕ `Array`.
 - Срез — это окно в исходный массив, БЕЗ копирования данных.
 - ОПАСНО: срез держит ссылку на ВЕСЬ массив.

 ❌ Ошибка:
 let slice = prices.prefix(3)  // ArraySlice

 ✅ Правильно:
 let topPrices = Array(prices.prefix(3))  // Array


 🧠 GOLDEN RULE for slices:

 - Массив —> для упорядоченных данных.
 - Если порядок НЕ важен —> используй Set.
 - Если нужна ЧАСТЬ массива — используй срез, но ОБЕРНИ в Array,
   чтобы освободить память.

 📌 КОГДА использовать ArraySlice:
 - Для быстрых операций БЕЗ копирования
 - Когда нужна только ЧАСТЬ данных
 - Но сразу превращай в Array, если срез живёт ДОЛГО
 */
