import Foundation
 
 /* 📋 ШПАРГАЛКА: КЛЮЧЕВЫЕ МЕТОДЫ МАССИВОВ В SWIFT
  
  🔹 ПОИСК:
  - firstIndex(of:) -> Находит индекс (позицию) первого совпадения.
  - last            -> Быстрый доступ к самому последнему элементу.
  - max() / min()   -> Автоматически находит самое большое/маленькое число.
  
  🔹 ИЗМЕНЕНИЕ И ПЕРЕСТАНОВКА:
  - swapAt(index1, index2) -> Безопасно меняет два элемента местами в памяти.
  - replaceSubrange(_:with:) -> Заменяет целый кусок массива другими элементами.
  
  🔹 СОРТИРОВКА И ПОРЯДОК (Отработано! ✅):
  - sort()       -> Сортирует оригинал прямо на месте (меняет исходный массив).
  - sorted(by:)  -> Возвращает новую отсортированную копию, оригинал не трогает.
  - reversed()   -> Разворачивает массив задом наперед (создает экономную "въюху").
  
  🔹 РАЗДЕЛЕНИЕ И СРЕЗЫ (Отработано! ✅):
  - prefix(_:)   -> Берет первые N элементов (возвращает ArraySlice!).
  - suffix(_:)   -> Берет последние N элементов (возвращает ArraySlice!).
  - split(separator:) -> Крутой метод для разбиения массива (или строк) по маркеру.
  
  🔹 ПРОВЕРКА (Отработано! ✅):
  - allSatisfy     -> Возвращает true, только если абсолютно ВСЕ элементы подошли.
  - contains(where:) -> Возвращает true, если подошел хотя бы ОДИН элемент.
 
  💡 Главное открытие: Ловушка ArraySlice и удержание памяти
  
 - НЕ знала:
  Что методы вроде `.prefix(3)` или `.suffix()` возвращают тип `ArraySlice`. Это не массив, а просто "окошко" в оригинальный массив.
 - В чем опасность:
  Этот срез держит ссылку на ВЕСЬ огромный исходный массив в памяти. Если срез живет долго — память не освобождается (утечка!).
 - Мой косяк 📐:
  Написала `Array(prices).prefix(3)` — это сделало копию всего массива ДО обрезки, и на выходе все равно остался срез.
 - Правильное решение:
  `let topCheapArray = Array(prices.prefix(3))` — инициализатор `Array()` должен стоять в самом конце конвейера, чтобы запечатать срез в новый, независимый от оригинала кусок памяти.
  
  
