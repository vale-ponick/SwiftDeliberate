import Foundation

/*
 📐🚕💨 ТЕОРИЯ: Массивы (Middle)
 
 ЧАСТЬ 1: под капотом, lazy и продвинутые методы.
 
 🛑 1. Главные правила использования — Три «НЕ»:
   1.1 🙅‍♂️ Не let —> свойства всегда д.б. var
   1.2 🧠 Не для computed properties —> вычисляемые свойства не кэшируют результат.
   1.3 🧵 Не потокобезопасно —> одновременный вызов из разных потоков дублирует инициализацию.

 🧩 2. Ленивые последовательности (lazy):
   2.1 ⚙️ - это модификатор, откладывающий создание объекта до ПЕРВОГО обращения.
   2.2 📦 Когда полезен? Тяжелые операции: обработка картинок, парсинг больших JSON, запросы к БД.
   2.3 🎯 Когда СТОИТ применять:
       - ⚡️ Оптимизация стартовой производительности (не трать ресурсы зря).
       - 🔗 Создание цепочек зависимостей внутри одного объекта через `self`.
       - 🔕 В редких сценариях, когда свойство может вообще НЕ пригодиться во время сессии.
   2.4 ⚠️ Когда НЕ СТОИТ использовать:
       - 🔄 Многократное чтение элементов (вызовет повторные тяжелые вычисления).
       - 🎲 Операции произвольного доступа (Random Access) и передача в скроллящийся UI (будет тормозить).
       - 🪶 Маленькие массивы (оверхед на поддержание ленивой структуры превысит выгоду).

 ⚔️ 3. В чем разница: array.map { ... } VS array.lazy.map { ... }
   3.1 🏃‍♂️ Обычный map:
       - Выполняет преобразование сразу для ВСЕХ элементов.
       - Сохраняет результат в НОВЫЙ массив.
       - 💾 Тип возвращаемого значения: Array<T>
   3.2 🦥 lazy.map:
       - НЕ выполняет никаких преобразований СРАЗУ.
       - Возвращает спец. «ленивую» обертку-последовательность.
       - Преобразование конкретного элемента происходит строго в МОМЕНТ обращения к нему.
       - 🚨 Результат вычисления НЕ кэшируется! При повторном обращении код выполнится заново.
       - 💾 Тип возвращаемого значения: LazyMapSequence<...>

 💎 4. Идеальный сценарий для lazy.map (Цепочки методов):
   Ленивые вычисления незаменимы, когда вы трансформируете массив, но в итоге нужен только ОДИН или несколько ПЕРВЫХ элементов (например, через .first).
*/

let namesIT = ["Alex", "John", "Anna", "Maria", "Peter"]

// 🟥 Вариант 1: С обычным map / filter
// 1. .map изменит ВСЕ 5 имен до верхнего регистра (создаст НОВЫЙ массив  в памяти).
// 2. .filter проверит ВСЕ 5 элементов (создаст еще один НОВЫЙ массив в памяти).
// 3. .first заберет первый элемент. ИТОГ: Выполнено много лишней работы!
let normalResult = namesIT.map { $0.uppercased() }
    .filter { $0.hasPrefix("A") }
    .first    // Результат: Optional("ALEX") — тип String?

// 🟩 Вариант 2: С использованием .lazy
// Идет по исходному массиву пошагово, экономя ресурсы:
// Возьмет "Alex" -> сделает "ALEX" -> проверит префикс "A" -> подошло!
// На этом работа ОСТАНОВИТСЯ. Имена "John", "Anna" и остальные даже НЕ будут обрабатываться.
let lazyResult = namesIT.lazy
    .map { $0.uppercased() }
    .filter { $0.hasPrefix("A") }
    .first
// Результат: Optional("ALEX") — тип String? */


 /* ЧАСТЬ 2: Оптимизация, сложность и вложенность.

 🔄 5. Метод reduce(into:) — более эффективный reduce:
   5.1 ⚔️ Отличие от обычного reduce:
       - Обычный `reduce` принимает КОПИЮ аккумулятора и возвращает новое значение на каждом шаге.
       - Метод `reduce(into:)` принимает аккумулятор как `inout` (по ссылке) и модифицирует его напрямую.
   5.2 ⚡️ Почему он быстрее?
        На больших структурах данных (массивы, словари) обычный `reduce` копирует ВСЮ коллекцию на КАЖДОЙ итерации, создавая дикую нагрузку на память (сложность $O(N2)$). `reduce(into:)` изменяет ОДНУ И ТУ ЖЕ коллекцию на месте, НЕ копируя её ($O(N)$).
   5.3 🎯 Когда использовать? Всегда, когда результатом свертки является 'коллекция' (массив, словарь, сет), а не простое число или строка.

 ⏱ 6. Производительность операций с массивами:
   6.1 🐢 Почему removeFirst() медленный?
            Массив в Swift — это непрерывный блок памяти. Удаление первого элемента ($O(N)$) заставляет процессор сдвигать абсолютно все оставшиеся элементы на одну ячейку влево.
   6.2 🚀 Почему removeLast() быстрый?
            Удаление с конца ($O(1)$) НЕ требует сдвига других элементов. Swift просто уменьшает счетчик количества элементов и мгновенно освобождает/забывает последнюю ячейку.
   6.3 📈 Что такое «амортизированная сложность»?
            Это среднее время выполнения операции, которая в редких случаях бывает тяжелой, но почти всегда — мгновенной. Например, `append()` обычно равен $O(1)$, но когда массив заполнен, Swift выделяет новый буфер памяти в 2 раза больше и копирует туда элементы ($O(N)$). В среднем же операция считается $O(1)$.
   6.4 🛠 Как это влияет на код?
            Если вы знаете точный или примерный размер массива заранее, используйте метод `reserveCapacity()`. Это исключит СКРЫТЫЕ ПЕРЕвыделения памяти процессором.

 🪆 7. Вложенные массивы (массивы массивов):
   7.1 📐 Как объявить? `[[Тип]]`
            (напр., `let matrix: [[Int]] = [[1, 2], [3, 4]]`).
   7.2 🔑 Как получить доступ?
            Используя последовательные индексы: `matrix[строка][колонка]`.
   7.3 📱 Где используется?
            - cоздание игровых сеток (шахматы, крестики-нолики),
            - разбиение данных для секций в `UITableView` / `UICollectionView`
            - работа с матрицами и координатами.

 🍕 8. ArraySlice:
   8.1 🧲 Почему ArraySlice держит ссылку на весь массив?
            Срез НЕ копирует элементы в память ради скорости ($O(1)$). Он хранит слабую ссылку на исходный массив и просто указывает на его часть. Из-за этого исходный массив *не удалится из памяти*, пока жив хотя бы один его маленький срез (🧲 утечка памяти!).
   8.2 🔄 Как правильно превратить срез в массив? Чтобы освободить память исходного массива, срез нужно принудительно скопировать в новый массив через инициализатор: `let newArray = Array(mySlice)`.
*/

// MARK: - 💻 ПРАКТИЧЕСКИЕ ПРИМЕРЫ

// 🟩 5. Пример работы reduce(into:)
let wordsIT = ["apple", "banana", "apple", "cherry", "banana", "banana"]
// Группируем слова и считаем их количество в словарь [String: Int]
let wordCount = wordsIT.reduce(into: [:]) { counts, word in
    counts[word, default: 0] += 1 // Модифицируем словарь counts по ссылке 'inout'
}
// Результат: ["apple": 2, "banana": 3, "cherry": 1]


