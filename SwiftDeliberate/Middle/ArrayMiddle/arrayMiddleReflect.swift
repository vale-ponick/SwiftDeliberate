 📌 Что запомнила:
 - `lazy` — для цепочек, где нужен `first` или `prefix`
 - Не использовать `lazy` для маленьких массивов
 - `lazy` не потокобезопасен
 
  ЧАСТЬ 2: Оптимизация, сложность и вложенность.
 
 🔥 Что поняла:
 - `reduce(into:)` быстрее, чем обычный `reduce` на больших данных
 - `removeLast()` — O(1), `removeFirst()` — O(n)
 - `ArraySlice` держит ссылку на весь массив — утечка памяти возможна

 🧠 Где застряла:
 - `reduce(into:)` — поняла, только когда написала пример с `wordCount`
