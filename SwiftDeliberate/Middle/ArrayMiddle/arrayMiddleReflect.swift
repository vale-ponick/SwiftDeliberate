 📐🚕💨 РЕФЛЕКСИЯ: Массивы (Middle)
 
 ЧАСТЬ 1: под капотом, lazy и продвинутые методы.
 
 🔥 Что поняла:
 - `lazy` откладывает выполнение до момента обращения
 - `lazy.map` + `first` обрабатывает только нужные элементы
 - `lazy` полезен, когда результат — не вся коллекция

 🧠 Где застряла:
 - Сначала не понимала, почему `lazy` не кэширует результат
