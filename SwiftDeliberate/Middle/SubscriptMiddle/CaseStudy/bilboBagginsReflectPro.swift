import Foundation
/**
 Архитектурная модель «Замок-Крепость» для ProBilboChest
 1. Компоненты защиты
 Внутренняя сокровищница (private var items)
 Массив скрыт за модификатором private. Прямой доступ извне невозможен — попытка обойти защиту вызовет ошибку компиляции.

 Единственный вход (subscript / update)
 Это единственный легальный способ взаимодействия с хранилищем. Любой запрос проходит через этот контролируемый интерфейс.

 Страж у ворот (guard items.indices.contains(index))
 Проверяет валидность индекса по карте существующих комнат (диапазон массива).

 2. Сценарии работы
 ✅ Индекс верный
 Страж опускает подъемный мост

 Доступ к сокровищнице разрешен

 Операция выполняется безопасно

 ❌ Индекс вне границ
 Железная решетка (guard else) блокирует проход

 Код не пытается обратиться к несуществующему индексу

 Отправляется сигнал ошибки (throw ChestError.outOfBounds)

 Приложение продолжает работу без краша

 3. Схема потока выполнения
 text
 ВНЕШНИЙ ЗАПРОС (try chest[index] / try chest.update(at:))
          │
          ▼
     ┌─────────────┐
     │ СТРАЖА У ВОРОТ │
     │ (проверка guard) │
     └──────┬──────┘
            │
     ┌──────┴──────┐
     ▼             ▼
    ДА           НЕТ
 (индекс      (вне границ)
  валиден)         │
     │             ▼
     │        ⛓️ РЕШЕТКА УПАЛА
     │        (guard else)
     │             │
     ▼             ▼
 [ПОДЪЕМНЫЙ    🌊 РОВ С ВОДОЙ
   МОСТ]       (путь закрыт)
     │             │
     ▼             ▼
 ┌─────────┐   🎯 ВЫСТРЕЛ СТРЕЛЫ
 │СОКРОВИЩ-│   (throw ChestError)
 │НИЦА     │         │
 │(items)  │         ▼
 └─────────┘   [БЛОК CATCH]
               (ошибка обработана)
 4. Технические характеристики кода
 Характеристика    Реализация
 Универсальность    Generics (Item) — работа с любыми типами
 Инкапсуляция    private var items — защита данных
 Безопасность    get throws — контролируемая обработка ошибок
 Стиль Swift    Анонимные аргументы, лаконичный синтаксис
 Интеграция    Sequence — поддержка циклов for-in
 5. Уровень разработчика (Middle+)
 Код демонстрирует:

 Архитектурное мышление: защита через инкапсуляцию, а не через надежду на корректный ввод

 Продакшн-подход: предотвращение крашей через контролируемые ошибки

 Чистый Swift: использование современных фич языка (throws в сабскриптах, generics, protocol conformance)

 Документированность: код понятен без дополнительных комментариев благодаря продуманной структуре

 Краткая суть: это не просто сундук с сокровищами, это архитектурный шаблон безопасности, где каждый запрос проходит через систему проверок, гарантирующую стабильность работы.
 */
