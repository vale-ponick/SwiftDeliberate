/// # ПОЛНАЯ ШПАРГАЛКА: .reduce — ВСЕ СЛУЧАИ
///
/// Метод `.reduce()` сворачивает (агрегирует) массив в одно единственное значение.
///
/// ### БАЗОВЫЙ СИНТАКСИС:
///
/// коллекция.reduce(начальное_значение) { аккумулятор, элемент in
///     // новая логика
/// }
/// *Или кратко:* `коллекция.reduce(начальное_значение, оператор)`
struct ReduceDocumentationSummary {
    // Этот блок служит текстовым справочником внутри структуры для чистоты архитектуры проекта
    
    /// 🍬 САХАР 1: Арифметика
    /// - Сумма: `[1,2,3].reduce(0, +)` -> 6
    /// - Произведение: `[1,2,3].reduce(1, *)` -> 6
    /// - Максимум: `reduce(Int.min) { max($0, $1) }`
    
    /// 🍬 САХАР 2: Работа со строками
    /// - Объединение: `["a","b"].reduce("") { $0 + $1 }` -> "ab"
    /// - С пробелом: `reduce("") { $0 + " " + $1 }`
    
    /// 🍬 САХАР 3: Работа с опционалами
    /// - Сумма (nil → 0): `reduce(0) { $0 + ($1 ?? 0) }` -> Расчет без nil за ОДИН проход
    /// - Строки (nil → ""): `reduce("") { $0 + ($1 ?? "") }`
    
    /// 🍬 САХАР 4: Условная логика
    /// - Кол-во чётных: `reduce(0) { $0 + ($1 % 2 == 0 ? 1 : 0) }`
    /// - Сумма элементов > 10: `reduce(0) { $0 + ($1 > 10 ? $1 : 0) }`
}

// MARK: - 🍬 САХАР 5: Работа со словарями (Группировка)

/// Группирует массив слов в словарь по их первой букве.
/// - Efficiency: O(n) за один проход.
func testGrouping() {
    let words = ["apple", "banana", "apricot", "blueberry"]
    
    let grouped = words.reduce(into: [String: [String]]()) { result, word in
        guard let firstChar = word.first else { return }
        let key = String(firstChar)
        result[key, default: []].append(word)
    }
    print(grouped) // ["a": ["apple", "apricot"], "b": ["banana", "blueberry"]]
}

// MARK: - 🍬 САХАР 6: Кастомные объекты

struct Dwarf {
    let name: String
    let age: Int
}

/// Вычисляет аналитику по массиву кастомных объектов Dwarf.
func testCustomObjects() {
    let dwarfs = [
        Dwarf(name: "Vale", age: 25),
        Dwarf(name: "John", age: 30),
        Dwarf(name: "Kate", age: 22)
    ]
    
    // 1. Средний возраст
    let avgAge = dwarfs.reduce(0) { $0 + $1.age } / dwarfs.count
    print("Средний возраст гномов: \(avgAge)") // 25
    
    // 2. Имена через запятую (с проверкой на первый элемент)
    let names = dwarfs.reduce("") { $0 + ($0.isEmpty ? "" : ", ") + $1.name }
    print("Список имен: \(names)") // "Vale, John, Kate"
}

// MARK: - 🏆 THE GOLDEN RULE & METRICS

/// ### ⚡️ ПРЕИМУЩЕСТВА И МЕТРИКИ МЕТОДА REDUCE:
/// * ✅ **Эффективность O(n):** Один единственный проход по массиву.
/// * ✅ **Оптимизация памяти:** Работает без выделения памяти под промежуточные массивы.
/// * ✅ **Универсальность:** Работает с любыми типами данных, опционалами и группировками.
///
/// ### 🧠 GOLDEN RULE:
/// 1. Простой массив без nil ➡️ `.reduce(0, +)`
/// 2. Массив с опционалами (nil) ➡️ `.reduce(0) { $0 + ($1 ?? 0) }`
/// 3. Сборка строк ➡️ `.reduce("") { $0 + $1 }`
/// 4. Группировка в коллекции ➡️ `.reduce(into: [:]) { ... }`

// MARK: - 📊 ШПАРГАЛКА: ВСЕ ИНСТРУМЕНТЫ

/*
 🔹 Сумма чисел:
    [1,2,3].reduce(0, +) → 6
 
 🔹 Произведение:
    [1,2,3].reduce(1, *) → 6
 
 🔹 Сборка строки:
    ["Hello", "World"].joined(separator: " ") → "Hello World"
 
 🔹 Убрать nil:
    [1, nil, 3].compactMap { $0 } → [1, 3]
 
 🔹 Группировка:
    reduce(into: [:]) { ... } → ["a": ["apple", "apricot"]]
 
 🔹 Трансформация:
    [1,2,3].map { $0 * 2 } → [2,4,6]
 
 🔹 Фильтрация:
    [1,2,3,4].filter { $0 % 2 == 0 } → [2,4]
 */

